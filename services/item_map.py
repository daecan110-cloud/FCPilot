_STRIP_PATTERNS = ["매년 반복지급(종신)", "매년반복지급(종신)", "매년 반복지급", "(종신)", "(1년)", "(매년)"]


def find_row_for_item(item_name: str):
    """항목명 → 템플릿 행 번호 (긴 키 우선 매칭)"""
    item_name = item_name.strip()
    if not item_name:
        return None
    for pat in _STRIP_PATTERNS:
        item_name = item_name.replace(pat, "").strip()

    if item_name in ITEM_ROW_MAP:
        return ITEM_ROW_MAP[item_name]

    best_row = None
    best_len = 0
    for key, row in ITEM_ROW_MAP.items():
        if row is None:
            continue
        if key in item_name or item_name in key:
            if len(key) > best_len:
                best_row = row
                best_len = len(key)

    if best_row is not None:
        return best_row

    for stype, row in SURGERY_TYPE_MAP.items():
        if stype in item_name and "수술" in item_name:
            return row

    return None
